export AGENT_SERVER_TTS_COSYVOICE_MODE="${TTS_MODE}"
export AGENT_SERVER_TTS_COSYVOICE_SPK_ID="${TTS_SPK_ID}"
export AGENT_SERVER_TTS_COSYVOICE_INSTRUCT_TEXT="${TTS_INSTRUCT_TEXT}"
export AGENT_SERVER_TTS_COSYVOICE_SOURCE_SAMPLE_RATE="${TTS_SOURCE_RATE}"

exec go run ./cmd/agentd
